public class CountingByHalves
{
    public static void main(String [] args)
    {
        
        
        System.out.println("   x   ");
        System.out.println("_________");
        
        for (float i=-10; i<=10; i+=0.5)
        {
            System.out.print(i + " \n");
        }
        
    }
}
