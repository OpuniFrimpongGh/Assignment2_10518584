public class ForLoopChallenge
{
    public static void main(String [] args)
    {
       
        System.out.println("I'm counting from 15 to 30 by 3s: ");
        int n1= 15;
        int n2=30;
        int n3= 3;
        
        
        for (int j=n1; j<=n2; j+=n3)
        {
            System.out.print(j + " ");
        }
        
    }
}
